    private void login() {
        if (frame.checkAuth(jTextField1.getText(), new String(jPasswordField1.getPassword()))) {
            if (jCheckBox1.isSelected()) {
                LocalSettingsFile set = new LocalSettingsFile();
                set.setLastUserID(mainframe.getUser().getId());
                set.save();
            }
            this.dispose();
        }
    }
