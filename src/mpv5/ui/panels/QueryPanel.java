    private void run() {
        try {
            ReturnValue data = QueryHandler.getConnection().freeSelectQuery(jTextArea1.getText(), MPSecurityManager.VIEW, null);
            if (data.hasData()) {
                jTable1.setModel(new MPTableModel(data.getData(), data.getColumnnames()));
            }
        } catch (Exception e) {
            Popup.error(e);
        }
    }
